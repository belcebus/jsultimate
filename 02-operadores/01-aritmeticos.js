let a = 5;
let b = 7;

console.log(a + b, 'adicion'); //adicion
console.log(a - b, 'resta'); //resta
console.log(a * b, 'multiplicacion'); //multiplicación
console.log(a / b, 'division'); //division
console.log(a % b, 'resto'); //módulo
console.log(a ** b, 'potencia'); //potencia

// incrementar
console.log(++a); //primero incrementa y luego usa a
/*
    6
*/
console.log(a++); //primero usa a y luego lo incrementa
/*
    6
*/
console.log(a)
/*
    7
*/

//decrementar
console.log(--a); //primero decrementa y luego usa a
/*
    6
*/
console.log(a--); //primero usa a y luego decrementa
/*
    6
*/
console.log(a)
/*
    5
*/